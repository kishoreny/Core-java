// Code Comments
/*
   Multiline Comments
   Line 1
   Line 2
*/

/*  
	This Program Prints a message: "The Print Statement"
*/

public class JavaPrint 
{
	   public static void main(String[] args)
	   {
		   System.out.println("System.out.println --> LN Prints this text and ADDs a line break");
		   System.out.println("System.out.print --> NO-LN Prints this text and DOES NOT ADD a line break");
		   System.out.print("Message 1 in this line");
		   System.out.print("Message 2 in the  same line");
		   System.out.print("\n");
		   System.out.println("Printing to console in Java: Hello Text !");
		   System.out.println("Single line Comments are placed using: //");
		   System.out.println("Multiline Comments are placed using: /* */");
		   System.out.println("/* My Comment Line 1");
		   System.out.println(" My Comment Line 2 */");
	   }
}
